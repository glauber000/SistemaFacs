    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String texto = caixa2.getText();
        boolean tt = true,tz = true;
        Professores ps;
        for(int i = 0; i < p.size() ; i++){
            if(texto.equalsIgnoreCase(p.get(i).getNome())){
                ps = p.get(i);
                ArrayList<Escolas> e = ps.getEscolas();
                ArrayList<Cursos> c = tg.getDisciplina().getCurso();
                tt = false;
                for(int j = 0; j<c.size();j++){
                    for(int z = 0; z<e.size();z++){
                        if(c.get(j).getEscola().getNome().equalsIgnoreCase(e.get(z).getNome())){
                            tg.setProfessor(ps);
                            tz = false;
                            break;
                        }
                    }
                }
                break;
            }

        }
        if(tz){
            f.setTxt("Professor n達o cadastrado na escola da disciplina!");
            f.setVisible(true);
        }
        if(tt){
            f.setTxt("Professor n達o existente tente outro!");
            f.setVisible(true);
        }
    }//GEN-LAST:event_jButton4ActionPerformed
