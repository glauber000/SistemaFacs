    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String texto = caixa1.getText();
        boolean tt = true;
        for(int i = 0; i < d.size() ; i++){
            if(texto.equalsIgnoreCase(d.get(i).getNome())){
                ts.setDisciplina(d.get(i));
                tt = false;
                break;
            }
        }
        if(tt){
           f.setTxt("Disciplina n達o existente tente outra!");
           f.setVisible(true); 
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String texto = caixa2.getText();
        boolean tt = true,tz = true;
        Professores ps;
        for(int i = 0; i < p.size() ; i++){
            if(texto.equalsIgnoreCase(p.get(i).getNome())){
                ps = p.get(i);
                ArrayList<Escolas> e = ps.getEscolas();
                ArrayList<Cursos> c = ts.getDisciplina().getCurso();
                tt = false;
                for(int j = 0; j<c.size();j++){
                    for(int z = 0; z<e.size();z++){
                        if(c.get(j).getEscola().getNome().equalsIgnoreCase(e.get(z).getNome())){
                            ts.setProfessor(ps);
                            tz = false;
                            break;
                        }
                    }
                }
                break;
            }
           
        }
        if(tz){
            f.setTxt("Professor n達o cadastrado na escola da disciplina!");
            f.setVisible(true);
        }
        if(tt){
           f.setTxt("Professor n達o existente tente outro!");
           f.setVisible(true); 
        }
    }//GEN-LAST:event_jButton4ActionPerformed
